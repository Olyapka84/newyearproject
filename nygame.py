def check_ready():
    while True:
        answer = input("\nНадеюсь, ты готов к празднику? Напиши \"да\", ответ \"нет\" не принимается! ").strip().lower()
        if answer == "да":
            break
        elif answer == "нет":
            print("Ну я же говорил, что 'нет' не принимается! :-)")
        else:
            print("Хо-хо-хо, но ответы, кроме \"да\", не принимаются! :-)\n")

def main():
    user_name = get_user_name()
    print(f"\nПривет, {user_name}!")
    check_ready()
    time.sleep(0.5)
    print("\nНачинаем обратный отсчёт до Нового года с фейерверками!")
    time.sleep(0.8)
    display_fireworks()
    new_year_countdown()
    tell_funny_resolution()
    print("\nА еще желаю отличного года, успешно завершить обучение и найти крутую работу! ❤️")

if __name__ == "__main__":
    main()
