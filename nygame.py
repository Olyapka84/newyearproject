        "Перестать пугаться слова 'декоратор'",
        "Не откладывать дедлайн на последний час перед сдачей!",
        "Выучить разницу между списком и кортежем без подсказок!",
        "Наконец-то разобраться с Git и не бояться merge-конфликтов!",
        "Попробовать объяснить OOP коту. Если он поймёт — значит, ты мастер!",
        "Не забывать коммитить код чаще, чем раз в месяц!",
        "Доделать свой пет-проект, даже если это калькулятор на Python!",
        "Сделать что-то на Flask и почувствовать себя настоящим бэкендером!",
        "Перестать бояться слова 'асинхронность'!",
        "Перестать считать, что 'try-except' — это способ решить все проблемы!",
