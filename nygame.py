import time
import random

def display_fireworks():
    fireworks = [
        "✨   ✨",
        "  ✨ ✨",
        "✨✨ ✨",
        " ✨✨  ✨",
        " ✨ ✨ ✨",
        "✨   ✨ ✨",
        "✨✨ ✨ ✨"
    ]
    for _ in range(10):
        print(random.choice(fireworks))
        time.sleep(0.2)


def get_user_name():
    print("Добро пожаловать в новогоднее приложение для юных питонистов! Я ИИ-Дед Мороз, подарки не дарю, но генерирую полезные пожелания!")
    name = input("Как тебя зовут?\n")
    return name

def new_year_countdown():
    print("\nГотовимся к Новому году!")
    for i in range(5, 0, -1):
        print(f"{i}...")
        time.sleep(0.5)
    print("С НОВЫМ ГОДОМ!!!")

